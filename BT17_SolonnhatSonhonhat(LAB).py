# Cho 3 số nguyên. Cho biết số lớn nhất và nhỏ nhất? 

a = int(input("Nhập vào số nguyên thứ nhất: "))
b = int(input("Nhập vào số nguyên thứ hai: "))
c = int(input("Nhập vào số nguyên thứ ba: "))

print("Số nguyên lớn nhất là: ", max(a, b, c))
print("Số nguyên nhỏ nhất là: ", min(a, b, c))