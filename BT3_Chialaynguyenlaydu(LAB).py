# Viết chương trình nhập vào 2 số nguyên dương a, b

a = int(input("Nhập vào số nguyên a: "))
b = int(input("Nhập vào số nguyên b: "))

# Cho biết kết quả chia lấy phần nguyên và phần dư của a với b.

# chia lấy phần nguyên
print("Kết quả chia lấy phần nguyên của a với b là: ", a//b)

# chia lấy phần dư
print("Kết quả chia lấy phần dư của a với b là: ", a%b )